package com.bin.soundcloud.Data;

public class SingleItemRecycleView_Vertical {
    private String imgbaihat;
    private  String tencasi;
    private  String tenbaihat;
    private  String dungluong;
    private  String timesong;

    public SingleItemRecycleView_Vertical() {
    }

    public SingleItemRecycleView_Vertical(String imgbaihat, String tencasi, String tenbaihat, String dungluong, String timesong) {
        this.imgbaihat = imgbaihat;
        this.tencasi = tencasi;
        this.tenbaihat = tenbaihat;
        this.dungluong = dungluong;
        this.timesong = timesong;
    }

    public String getImgbaihat() {
        return imgbaihat;
    }

    public void setImgbaihat(String imgbaihat) {
        this.imgbaihat = imgbaihat;
    }

    public String getTencasi() {
        return tencasi;
    }

    public void setTencasi(String tencasi) {
        this.tencasi = tencasi;
    }

    public String getTenbaihat() {
        return tenbaihat;
    }

    public void setTenbaihat(String tenbaihat) {
        this.tenbaihat = tenbaihat;
    }

    public String getDungluong() {
        return dungluong;
    }

    public void setDungluong(String dungluong) {
        this.dungluong = dungluong;
    }

    public String getTimesong() {
        return timesong;
    }

    public void setTimesong(String timesong) {
        this.timesong = timesong;
    }
}
